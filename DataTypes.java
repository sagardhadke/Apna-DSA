public class DataTypes {
    public static void main(String args[]){
        //DataTypes in Java 

        //Primitive
        int a = 210210;
        float b = 20.23f;
        double c = 56.35;
        short d = 6461;
        long e = 4785241351351316464L;
        char f = 'S';
        byte g = 124;
        boolean h = true;

        //Non-Primitive
        String name = "Sagar";
        /* 
         * Arrays
         * Class
         * Object
         * Interface
         */


        //All DataTypes Max and min Values
        long getMin = Long.MIN_VALUE;
        long getMax = Long.MAX_VALUE;
        System.out.println(getMin);
        System.out.println(getMax);

        //All DataTypes Minimum and Maximum Values.

        // int => -2147483648 To 2147483647
        // Float => 1.4E-45 To 3.4028235E38
        // Double => 4.9E-324 To 1.7976931348623157E308
        // Char => 'a-z' to 'A-z'
        // boolean => true and false
        // bytes => -128 To 127
        // short => -32768 To 32767
        // Long => -9223372036854775808 To 9223372036854775807

 
    }
}
