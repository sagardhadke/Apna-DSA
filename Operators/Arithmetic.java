package Operators;

public class Arithmetic {
    public static void main(String[] args) {
        //Arithmetic Operators
        
        int a = 25;
        int b = 32;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
        
    }
}
